package chapterTwoAndThree;

public class Arithmetic {


    public int productOfIntegers(int firstNumber, int secondNumber) {
        int productOfInt = firstNumber * secondNumber;
        return productOfInt;
    }

    public  int differenceOfIntegers(int firstNumber, int secondNumber) {
        int diffOfInt = firstNumber - secondNumber;
        return diffOfInt;
    }

    public  int sumOfIntegers (int firstNumber, int secondNumber) {
        int sumOfInt = firstNumber + secondNumber;
        return sumOfInt;
    }
}
