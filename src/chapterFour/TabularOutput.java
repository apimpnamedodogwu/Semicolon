package chapterFour;

public class TabularOutput {
    public static void main(String[] args) {
        int count = 1;

        while (count <= 5) {

            System.out.println(count);
            System.out.println(count * count);
            System.out.println(count * count * count);
            System.out.println(count * count * count * count);
            count++;
        }

    }



}
